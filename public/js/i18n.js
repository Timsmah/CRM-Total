// ── Lang state ────────────────────────────────────────────────────────────────
let _lang = localStorage.getItem('crm_lang') || 'en';

function t(key) {
  return (I18N[_lang]?.[key]) ?? (I18N.en?.[key]) ?? key;
}

function getLang() { return _lang; }

function setLang(lang) {
  _lang = lang;
  localStorage.setItem('crm_lang', lang);

  // Update toggle button text
  const btn = document.getElementById('lang-toggle');
  if (btn) btn.textContent = lang === 'en' ? '🇫🇷 Français' : '🇬🇧 English';

  // Update static nav labels
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });

  // Re-navigate to current section to refresh all dynamic content
  if (typeof Router !== 'undefined') {
    Router.navigate(Router.current || 'clients');
  }
}
